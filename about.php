          <h2 class="txt section-title"><strong>History.</strong></h2>
          <p class="text-dark font-weight-bold">The Ganges River, also called Ganga, is a river located in northern India that flows toward the border with Bangladesh. It is the longest river in India and flows for around 1,569 miles (2,525 km) from the Himalayan Mountains to the Bay of Bengal. The river has the second greatest water discharge in the world, and its basin is the most heavily populated in the world with over 400 million people living in the basin.
          The Ganges River is extremely important to the people of India as most of the people living on its banks use it for daily needs. It is also significant to Hindus as they consider it their most sacred river.</p>
          <h2><strong>The Course of the Ganges River</strong></h2>

          <p class="text-dark font-weight-bold">The headwaters of the Ganges River begin high in the Himalayan Mountains where the Bhagirathi River flows out of the Gangotri Glacier in India's Uttarakhand state. The glacier sits at an elevation of 12,769 feet (3,892 m). The Ganges River proper begins farther downstream where the Bhagirathi and Alaknanda rivers join. As the Ganges flows out of the Himalayas, it creates a narrow, rugged canyon.
          The Ganges River emerges from the Himalayas at the town of Rishikesh where it begins to flow into the Indo-Gangetic Plain. This area, also called the North Indian River Plain, is a very large, relatively flat, fertile plain that makes up most of the northern and eastern parts of India as well as parts of Pakistan, Nepal, and Bangladesh. In addition to entering the Indo-Gangetic Plain in this area, part of the Ganges River is also diverted toward the Ganges Canal for irrigation in the Uttar Pradesh state.
          As the Ganges River then flows farther downstream, it changes its direction several times and is joined by many other tributary rivers such as the Ramganga, Tamsa, and Gandaki rivers to name a few. There are also several cities and towns that the Ganges River passes through on its way downstream. Some of these include Chunar, Kolkata, Mirzapur, and Varanasi. Many Hindus visit the Ganges River in Varanasi as that city is considered the holiest of cities. As such, the city's culture is also closely tied into the river as it is the most sacred river in Hinduism.</p>

          <p class="text-dark font-weight-bold">Once the Ganges River flows out of India and into Bangladesh, its main branch is known as the Padma River. The Padma River is joined downstream by large rivers like the Jamuna and Meghna rivers. After joining the Meghna, it takes on that name before flowing into the Bay of Bengal. Before entering the Bay of Bengal however, the river creates the world's largest delta, Ganges Delta. This region is a highly fertile sediment-laden area that covers 23,000 square miles (59,000 sq km).</p>

          <p class="text-dark font-weight-bold">It should be noted that the course of the Ganges River described in the above paragraphs is a general description of the river's route from its source where the Bhagirathi and Alaknanda rivers join to its outlet at the Bay of Bengal. The Ganges has very complicated hydrology, and there are several different descriptions of its overall length and the size of its drainage basin based on what tributary rivers are included. The most widely accepted length of the Ganges River is 1,569 miles (2,525 km), and its drainage basin is estimated to be about 416,990 square miles (1,080,000 sq km).</p>
          <h2><strong>The Population of the Ganges River.</strong></h2>

          <p class="text-dark font-weight-bold">The Ganges River basin has been inhabited by humans since ancient times. The first people in the region were of the Harappan civilization. They moved into the Ganges River basin from the Indus River basin around the 2nd millennium B.C.E. Later the Gangetic Plain became the center of the Maurya Empire and then the Mughal Empire. The first European to discuss the Ganges River was Megasthenes in his work Indica.
          In modern times the Ganges River has become a source of life for the nearly 400 million people living in its basin. They rely on the river for their daily needs such as drinking water supplies and food and irrigation and manufacturing. Today the Ganges River basin is the most populated in the world. It has a population density of about 1,000 people per square mile (390 per sq km).</p>
          <h2><strong>The Significance of the Ganges River.</strong></h2>

          <p class="text-dark font-weight-bold">Aside from providing drinking water and irrigating fields, the Ganges River is extremely important to India's Hindu population for religious reasons as well. The Ganges River is considered their most sacred river, and it is worshiped as the goddess Ganga Ma or "Mother Ganges." 
          According to the Myth of the Ganges, the goddess Ganga descended from heaven to dwell in the waters of the Ganges River to protect, purify and bring to heaven those who touch it. Devout Hindus visit the river daily to offer flowers and food to Ganga. They also drink the water and bathe in the river to cleanse and purify their sins. Also, Hindus believe that upon death the waters of the Ganges River are needed to reach the World of the Ancestors, Pitriloka. As a result, Hindus bring their dead to the river for cremation along its banks and afterward their ashes are spread in the river. In some cases, corpses are also thrown into the river. The city of Varanasi is the holiest of cities along the Ganges River and many Hindus travel there place ashes of their dead in the river.</p>

          <p class="text-dark font-weight-bold">Along with daily baths in the Ganges River and offerings to the goddess Ganga there are large religious festivals that occur in the river throughout the year where millions of people travel to the river to bathe so that they can be purified of their sins.
          <h2><strong>Pollution of the Ganges River.</strong></h2>

          <p class="text-dark font-weight-bold">Despite the religious significance and daily importance of the Ganges River for the people of India, it is one of the most polluted rivers in the world. Pollution of the Ganges is caused by both human and industrial waste due to India's rapid growth as well as religious events. India currently has a population of over one billion people, and 400 million of them live in the Ganges River basin. As a result, much of their waste, including raw sewage is dumped into the river. Also, many people bathe and use the river to clean their laundry. Fecal coliform bacteria levels near Varanasi are at least 3,000 times higher than what is established by the World Health Organization as safe (Hammer, 2007).</p>

          <p class="text-dark font-weight-bold">Industrial practices in India also have little regulation and as the population grows these industries do as well. There are many tanneries, chemical plants, textile mills, distilleries and slaughterhouses along the river and many of them dump their untreated and often toxic waste into the river. The water of the Ganges has been tested to contain high levels of things like chromium sulfate, arsenic, cadmium, mercury and sulfuric acid (Hammer, 2007).
          In addition to human and industrial waste, some religious activities also increase the pollution of the Ganges. For example, Hindus believe that they must make offerings of food and other items to Ganga and as a result, these items are thrown into the river regularly and more so during religious events. Human remains are also often placed into the river.</p>

          <p class="text-dark font-weight-bold">In the late 1980s India's prime minister, Rajiv Gandhi began the Ganga Action Plan (GAP) to clean up the Ganges River. The plan shut down many highly polluting industrial plants along the river, and allotted funding for the construction of wastewater treatment facilities but its efforts have fallen short as the plants are not large enough to handle the waste coming from such a large population (Hammer, 2007). Many of the polluting industrial plants are also continuing to dump their hazardous waste into the river.</p>

          <p class="text-dark font-weight-bold">Despite this pollution, however, the Ganges River remains important to the Indian people as well as different species of plants and animals such as the Ganges River dolphin, a very rare species of freshwater dolphin that is native only to that area. 
          </p>
